class Name:
    def __init__(self, fname, lname):
        self.fname = fname
        self.lname = lname

    
    def getFname(self):
        return self.fname
    
    
    def getLname(self):
        return self.lname
    
    def setFname(self, fname):
        self.fname = fname

    def setLname(self, lname):
        self.lname = lname




